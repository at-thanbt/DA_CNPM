package model.bean;

public class Vacxin {

	String ma;
	String ten;
	int sl;
	String mt;
	int gia;
	String tensx;
	public String getMa() {
		return ma;
	}
	public void setMa(String ma) {
		this.ma = ma;
	}
	public String getTen() {
		return ten;
	}
	public void setTen(String ten) {
		this.ten = ten;
	}
	public int getSl() {
		return sl;
	}
	public void setSl(int sl) {
		this.sl = sl;
	}
	public String getMt() {
		return mt;
	}
	public void setMt(String mt) {
		this.mt = mt;
	}
	public int getGia() {
		return gia;
	}
	public void setGia(int gia) {
		this.gia = gia;
	}
	public String getTensx() {
		return tensx;
	}
	public void setTensx(String tensx) {
		this.tensx = tensx;
	}
}
